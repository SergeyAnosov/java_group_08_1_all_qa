package ru.yandex.qa;

public class MainElseIfStarter {

    public static void main(String[] args) {
        int k = 254654;

        if (k == 1) {
            System.out.println("k = 1");
        } else if (k == 2) {
            System.out.println("k = 2");
        } else {
            System.out.println("Не один случай не подошел");
        }
    }
}
